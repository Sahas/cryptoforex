package com.crypto.forex.entities;


public class CoinPrice {
	
	private String coin;
	private Double price;
	
	public String getCoin() {
		return coin;
	}
	
	public void setCoin(String coin) {
		this.coin = coin;
	}
	
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
}
